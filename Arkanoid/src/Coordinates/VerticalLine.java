package Coordinates;

/**
 * The type Vertical line.
 *
 * @author Bar Aharon bar.aharon98@gmail.com
 * @version 1.0
 * @since 2022 -08-15.
 */
public class VerticalLine {
    private double xLine;

    /**
     * Instantiates a new Vertical line.
     *
     * @param xLine the x line
     */
    public VerticalLine(double xLine) {
        this.xLine = xLine;
    }

    /**
     * Gets x line.
     *
     * @return the x line
     */
    public double getXLine() {
        return xLine;
    }

    /**
     * Sets x line.
     *
     * @param xLine the x line
     */
    public void setXLine(double xLine) {
        this.xLine = xLine;
    }
}

